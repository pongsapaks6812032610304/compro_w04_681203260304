#include <stdio.h>  //ใช้สำหรับเรียกฟังก์ชัน Print() จาก satandard l/o library

int main() {        // 'int' = keyword, 'main' = ldentier, '()' = operator

    printf("Hello, \nc Token!\n"); // 'printf' = ldentifier, ข้อความใน"" = constant, '/n = constant

    return 0; // 'return = Keyword, '0' = constant, ';' = operator
}
/*Token type    ใช้อยู่ในโค้ด
keyword int, return
ldentifier  main, printf
constant "Hello,\n", "c Tokens!\n" , 0
operator    (), ;,= */
